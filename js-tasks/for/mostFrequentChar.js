// Задача: Поиск самой частой буквы
// Условие:
// Напиши функцию, которая принимает строку и возвращает ту букву, которая встречается чаще всего.
// Используй цикл for и объект (или Map) для подсчёта.

// Пример:
// Ввод: "javascript"
// Вывод: "a" (буква 'a' встречается 2 раза)

// Ограничения:
// Игнорируй регистр (преобразуй к нижнему), пробелы не считай.
// Если несколько букв встречаются одинаково часто — верни любую из них.
function mostFrequentChar(str) {
    const counts = {}

    for (let i = 0; i < str.length; i++) {
        const char = str[i].toLowerCase()
        if (char === ' ') continue
        counts[char] = (counts[char] || 0) + 1
    }

    let maxChar = ''
    let maxCount = 0

    for (const key in counts) {
        if (counts[key] > maxCount) {
            maxCount = counts[key]
            maxChar = key
        }
    }

    return maxChar
}

console.log(mostFrequentChar("javascript")) // → 'a'
